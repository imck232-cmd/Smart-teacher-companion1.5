const CreativeIdeas: React.FC<{ onBack: () => void }> = ({ onBack }) => {
    const [copiedId, setCopiedId] = useState<number | null>(null);
    // Initialize with only the first category open to reduce clutter
    const [openCategories, setOpenCategories] = useState<Record<number, boolean>>({ 0: true });

    const handleCopy = (text: string, id: number) => {
        navigator.clipboard.writeText(text);
        setCopiedId(id);
        setTimeout(() => setCopiedId(null), 2000);
    };

    const toggleCategory = (index: number) => {
        setOpenCategories(prev => ({
            ...prev,
            [index]: !prev[index]
        }));
    };

    return (
        <div className="animate-fadeIn pb-12">
            <ToolHeader title="أطلق العنان لإبداعك" onBack={onBack} />
            
            <div className="space-y-8">
                {categories.map((category, index) => {
                    const isOpen = openCategories[index];
                    return (
                        <div key={index} className="relative group rounded-3xl overflow-hidden shadow-xl bg-white dark:bg-slate-900 transition-all duration-300 hover:shadow-2xl">
                            
                            {/* Header - Clickable to toggle */}
                            <div 
                                className={`bg-gradient-to-r ${category.colorClass} p-6 md:p-8 text-white relative overflow-hidden cursor-pointer select-none`}
                                onClick={() => toggleCategory(index)}
                            >
                                {/* Background Decor */}
                                <div className="absolute -right-10 -top-10 text-white/10 text-[150px]">
                                    <i className={category.icon}></i>
                                </div>

                                <div className="relative z-10">
                                    <div className="flex flex-col md:flex-row justify-between items-start md:items-center gap-4 mb-4">
                                        <div className="flex items-center gap-4">
                                            {/* Toggle Button */}
                                            <button 
                                                onClick={(e) => { e.stopPropagation(); toggleCategory(index); }}
                                                className="w-10 h-10 rounded-full bg-white/20 hover:bg-white/30 flex items-center justify-center transition-all focus:outline-none focus:ring-2 focus:ring-white/50"
                                            >
                                                <i className={`fas fa-chevron-${isOpen ? 'up' : 'down'} transition-transform duration-300`}></i>
                                            </button>

                                            <h3 className="text-2xl md:text-3xl font-bold font-heading flex items-center gap-3">
                                                <i className={`${category.icon} text-2xl opacity-90`}></i>
                                                {category.title}
                                            </h3>
                                        </div>
                                        
                                        {/* AI Tools Navigation Bar */}
                                        <div 
                                            className="flex flex-wrap gap-2 bg-white/10 backdrop-blur-md p-2 rounded-full border border-white/20"
                                            onClick={(e) => e.stopPropagation()} // Stop propagation to prevent toggling when clicking tools
                                        >
                                            {aiToolsLinks.map((tool, toolIdx) => (
                                                <a 
                                                    key={toolIdx}
                                                    href={tool.url}
                                                    target="_blank"
                                                    rel="noopener noreferrer"
                                                    data-tooltip={tool.name}
                                                    className={`w-10 h-10 rounded-full flex items-center justify-center bg-white/90 text-slate-700 transition-all duration-200 hover:scale-110 hover:shadow-lg ${tool.color}`}
                                                    title={tool.name}
                                                >
                                                    <i className={tool.icon}></i>
                                                </a>
                                            ))}
                                        </div>
                                    </div>
                                    <p className={`text-lg opacity-95 font-light leading-relaxed max-w-4xl transition-all duration-300 ${isOpen ? 'block' : 'hidden md:block'}`}>
                                        {category.goal}
                                    </p>
                                </div>
                            </div>
                            
                            {/* Items Grid - Conditionally Rendered */}
                            {isOpen && (
                                <div className="p-6 md:p-8 bg-gray-50 dark:bg-slate-800/50 animate-fadeIn">
                                    <div className="grid grid-cols-1 lg:grid-cols-2 gap-6">
                                        {category.items.map((item) => (
                                            <div 
                                                key={item.id} 
                                                className="flex flex-col justify-between bg-white dark:bg-slate-800 p-6 rounded-2xl shadow-sm hover:shadow-md border border-gray-100 dark:border-slate-700 transition-all duration-300 hover:border-primary/30 group/card"
                                            >
                                                <div className="flex gap-4">
                                                    <div className={`
                                                        flex-shrink-0 w-10 h-10 rounded-xl flex items-center justify-center text-white font-bold shadow-lg
                                                        bg-gradient-to-br ${category.colorClass}
                                                    `}>
                                                        {item.id}
                                                    </div>
                                                    <p className="text-base-text text-lg leading-relaxed font-medium">
                                                        {item.text}
                                                    </p>
                                                </div>
                                                
                                                <div className="flex justify-end mt-4 opacity-0 group-hover/card:opacity-100 transition-opacity duration-300">
                                                    <button
                                                        onClick={() => handleCopy(item.text, item.id + (index * 100))}
                                                        className={`
                                                            flex items-center gap-2 px-4 py-2 rounded-lg text-sm font-bold transition-all duration-300
                                                            ${copiedId === item.id + (index * 100) 
                                                                ? 'bg-green-500 text-white shadow-lg' 
                                                                : 'bg-gray-100 dark:bg-slate-700 text-gray-600 dark:text-gray-300 hover:bg-primary hover:text-white'}
                                                        `}
                                                    >
                                                        {copiedId === item.id + (index * 100) ? (
                                                            <>
                                                                <i className="fas fa-check"></i> تم النسخ
                                                            </>
                                                        ) : (
                                                            <>
                                                                <i className="fas fa-copy"></i> نسخ الفكرة
                                                            </>
                                                        )}
                                                    </button>
                                                </div>
                                            </div>
                                        ))}
                                    </div>
                                </div>
                            )}
                        </div>
                    );
                })}
            </div>
        </div>
    );
};

export default CreativeIdeas;
